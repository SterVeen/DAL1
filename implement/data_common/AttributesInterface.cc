#include "AttributesInterface.h"

namespace DAL { // Namespace DAL -- begin
  
  // ============================================================================
  //
  //  Destruction
  //
  // ============================================================================
  
  AttributesInterface::~AttributesInterface ()
  {
    destroy();
  }
  
  void AttributesInterface::destroy ()
  {;}
  
  // ============================================================================
  //
  //  Operators
  //
  // ============================================================================
  
  //_____________________________________________________________________________
  //                                                                    operator=
  
  /*!
    \param other -- Another AttributesInterface object from which to make a copy.
  */
  AttributesInterface& AttributesInterface::operator= (AttributesInterface const &other)
  {
    if (this != &other) {
      destroy ();
      copy (other);
    }
    return *this;
  }
  
  //_____________________________________________________________________________
  //                                                                         copy
  
  void AttributesInterface::copy (AttributesInterface const &other)
  {
    itsAttributes.clear();
    
    if (!other.itsAttributes.empty()) {
      itsAttributes = other.itsAttributes;
    }
  }

} // Namespace DAL -- end
